#include "utils.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	unsigned char	*ts1;
	unsigned char	*ts2;

	if (!n)
		return (0);
	ts1 = (unsigned char *)s1;
	ts2 = (unsigned char *)s2;
	while (*ts1 && *ts2 && --n && *ts1 == *ts2)
	{
		ts1++;
		ts2++;
	}
	return ((unsigned char)*ts1 - (unsigned char)*ts2);
}
